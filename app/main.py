from fastapi import FastAPI
from fastapi.middleware.cors import CORSMiddleware
from .api import dashboard

app = FastAPI(title="Legatora Dashboard API", version="0.1.0")

# Allow requests from typical frontend dev hosts (adjust in production)
app.add_middleware(
    CORSMiddleware,
    allow_origins=["http://localhost:3000", "http://localhost:5173"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

app.include_router(dashboard.router)


@app.get("/health")
def health():
    return {"status": "ok"}
